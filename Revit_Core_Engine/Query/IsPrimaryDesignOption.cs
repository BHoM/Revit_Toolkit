using System.ComponentModel;
using Autodesk.Revit.DB;
using BH.oM.Base.Attributes;

namespace BH.Revit.Engine.Core
{
    public static partial class Query
    {
        /***************************************************/
        /****              Public methods               ****/
        /***************************************************/

        [Description("Checks whether a given Revit element is within the primary design option.")]
        [Input("element", "Revit element to be checked whether it is within the primary design option.")]
        [Output("primaryDesignOption", "True if the input Revit element is within the primary design option, otherwise false.")]
        public static bool IsPrimaryDesignOption(this Element element)
        {
            return element.DesignOption?.IsPrimary != false;
        }

        /***************************************************/
    }
}


